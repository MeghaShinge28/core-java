class WideCasting 
{
	public static void main(String[] args) 
	{
		int a = 7;
		int b = 10;
		System.out.println("Before Wide Casting:");
		System.out.println(a);
		System.out.println(b);
		double c = a; 
		double d = b;
		System.out.println("\nAfter Wide Casting:");
		System.out.println(c);
		System.out.println(d);
	}
}